export function normalizeLocale(locale) {
  if (typeof locale !== "string") return DEFAULT_LOCALE;

  const normalized = locale.toLowerCase();
  if (LOCALES.includes(normalized)) return normalized;

  if (normalized.startsWith("ru")) return "ru";
  if (normalized.startsWith("en")) return "en";

  return DEFAULT_LOCALE;
}

export function getDictionary(locale) {
  return dictionaries[normalizeLocale(locale)] || dictionaries[DEFAULT_LOCALE];
}

function interpolate(template, values = {}) {
  if (typeof template !== "string") return template;
  if (!values || typeof values !== "object") return template;

  return template.replace(/\{(\w+)\}/g, (match, token) => {
    if (Object.prototype.hasOwnProperty.call(values, token)) {
      return String(values[token]);
    }
    return match;
  });
}

export function translate(locale, key, values) {
  const dictionary = getDictionary(locale);
  const fallbackDictionary = dictionaries[DEFAULT_LOCALE];

  if (typeof key !== "string") return key;

  const template = Object.prototype.hasOwnProperty.call(dictionary, key)
    ? dictionary[key]
    : Object.prototype.hasOwnProperty.call(fallbackDictionary, key)
      ? fallbackDictionary[key]
      : key;

  return interpolate(template, values);
}
